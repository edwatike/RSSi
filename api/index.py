import os
import asyncio
from telegram import Bot
import logging
from bot import check_feeds

# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# –ü–æ–ª—É—á–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
TOKEN = os.getenv("TOKEN")
CHAT_ID = os.getenv("CHAT_ID")

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è Vercel (–æ–±—Ä–∞–±–æ—Ç—á–∏–∫ HTTP-–∑–∞–ø—Ä–æ—Å–æ–≤)
async def handler(request):
    try:
        logger.info("Received request on /api/check")
        
        # –í—ã–∑–æ–≤ check_feeds
        check_feeds()
        
        # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –±–æ—Ç–∞
        bot = Bot(token=TOKEN)
        
        # –û—Ç–ø—Ä–∞–≤–∫–∞ —Ç–µ—Å—Ç–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ —á–∞—Ç (–∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–æ)
        await bot.send_message(chat_id=CHAT_ID, text="Server is running! üöÄ")
        
        return {
            "statusCode": 200,
            "body": "Request processed successfully!"
        }
    except Exception as e:
        logger.error(f"Error: {e}")
        return {
            "statusCode": 500,
            "body": f"Error: {e}"
        }

# Vercel —Ç—Ä–µ–±—É–µ—Ç —Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–π –æ–±—Ä–∞–±–æ—Ç—á–∏–∫, –ø–æ—ç—Ç–æ–º—É –æ–±–æ—Ä–∞—á–∏–≤–∞–µ–º –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–π –∫–æ–¥
def sync_handler(request):
    return asyncio.run(handler(request))